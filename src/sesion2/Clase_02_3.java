public class Clase_02_3 {

    // operadores
    public static void main(String[] args) {

        int numero = 10; // Declarar una variable entera

        // Operador de suma y asignación
        numero += 2; // Equivale a numero = numero + 2
        System.out.println("Después de sumar 2: " + numero);

        // Operador de resta y asignación
        numero -= 3; // Equivale a numero = numero - 3
        System.out.println("Después de restar 3: " + numero);

        // Operador de multiplicación y asignación
        numero *= 4; // Equivale a numero = numero * 4
        System.out.println("Después de multiplicar por 4: " + numero);

        // Operador de división y asignación
        numero /= 2; // Equivale a numero = numero / 2
        System.out.println("Después de dividir entre 2: " + numero);

        // Operador de módulo y asignación
        numero %= 5; // Equivale a numero = numero % 5
        System.out.println("Después de calcular el módulo 5: " + numero);
    }

}
