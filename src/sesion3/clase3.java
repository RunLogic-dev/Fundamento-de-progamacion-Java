public class clase3 {
   // contar hasta  10
    
    public static void main(String[] args) {
         int i = 1;  // Inicializamos el contador

        // Bucle while: continuará mientras i sea menor o igual a 10
        while (i <= 10) {
            System.out.println(i);
            i++;  // Incrementamos el valor de i en cada iteración
        }
//    La variable i comienza en 1.
//El bucle ejecuta el código dentro del bloque mientras la condición i <= 10 sea verdadera.
//En cada iteración, i se incrementa, y cuando llega a 11, la condición ya no es verdadera,
//por lo que el bucle termina.
    }
}
